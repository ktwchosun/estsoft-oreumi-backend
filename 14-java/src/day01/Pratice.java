package day01;

import java.sql.SQLOutput;

public class Pratice {
    public static void main(String[] args) {
        int x = 10;
        int y = 3;
        System.out.printf("x: %d, y: %d\n", x, y);
        System.out.printf("x + y = %d\n", x + y);
        System.out.printf("x - y = %d\n", x - y);
        System.out.printf("x * y = %d\n", x * y);
        System.out.printf("x / y = %.2f\n", (double)x / y);
        System.out.printf("x %% y = %d\n", x % y);
    }
}
